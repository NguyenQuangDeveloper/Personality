namespace VSLibrary.Common.Log;

/// <summary>
/// Enumerates the log message types.
/// </summary>
public enum LogType
{
    /// <summary>
    /// Informational log message.
    /// </summary>
    Info = 0,

    /// <summary>
    /// Warning log message.
    /// </summary>
    Warn = 1,

    /// <summary>
    /// Debug log message.
    /// </summary>
    Debug = 2,

    /// <summary>
    /// Error log message.
    /// </summary>
    Error = 3
}
